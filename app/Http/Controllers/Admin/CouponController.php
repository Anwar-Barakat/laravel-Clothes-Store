    public function updateCouponStatus(Request $request)
    {
        if ($request->ajax()) {
            $data = $request->only(['status', 'coupon_id']);
            if ($data['status'] == 1)
                $status = 0;
            else
                $status = 1;
            Coupon::where('id', $data['coupon_id'])->update([
                'status'    => $status
            ]);
            return response()->json([
                'status'        => $status,
                'coupon_id'    => $data['coupon_id']
            ]);
        }
    }
